package src.solver;

public class Pair
{
    private double p1;
    private double p2;

    public Pair(double p1, double p2)
    {
        this.p1 = p1;
        this.p2 = p2;
    }

    double getFirst()
    {
        return this.p1;
    }

    double getSecond()
    {
        return this.p2;
    }

}
